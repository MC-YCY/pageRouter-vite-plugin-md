const computedDataLabel = (data:any,routerKey = null) => {
    data.map((item:any) => {
        let _routerKey = item.routerKey;
        if(routerKey){
            _routerKey = routerKey + '_' + item.routerKey;
        }
        item['key'] = _routerKey;
        item['label'] = item.name
        item['title'] = item.name
        if (item.children && item.children.length) computedDataLabel(item.children,_routerKey)
    })
}
